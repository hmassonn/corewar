#include "libft.h"

int			ft_strarrcmp(char *str, char **tab)
{
	int		i;

	i = 0;
	while (tab[i])
	{
		if (ft_strcmp(str, tab[i]) == 0)
			return (1);
		i++;
	}
	return (0);
}
